import sys

num = int(input())

def star(x, y):
    while(x !=0):
        if(x%3 == 1 and y%3 == 1):
            sys.stdout.write(' ')
            return None
        x = x // 3 #몫
        y = y // 3
    sys.stdout.write('*')

for x in range(num):
    for y in range(num):
        star(x, y)
    sys.stdout.write('\n')

